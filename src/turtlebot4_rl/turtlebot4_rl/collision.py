def point_in_obstacle(x, y):
    """
    判断点 (x, y) 是否在任意一个障碍物内。
    返回 True 表示在障碍物内，False 表示不在。
    """
    for ox, oy, w, h in obstacles:
        if ox <= x <= ox + w and oy <= y <= oy + h:
            return True
    return False
